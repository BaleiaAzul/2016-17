import Adafruit_PCA9685
import time

pwm = Adafruit_PCA9685.PCA9685(address=0x60, busnum=1)
pwm.set_pwm_freq(60)
pwm.set_pwm(9, 0, 4096)
pwm.set_pwm(10, 4096, 0)
while True:
    print(str(0) + str(4096))
    pwm.set_pwm(8, 0, 4096)
